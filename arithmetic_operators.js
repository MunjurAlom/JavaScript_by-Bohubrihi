// Arithmetic Operators
var num1, num2, result;
num1 = 25;
num2 = 7;
// Summation, +;
result = num1 + num2;
document.write("Summation of two Numbers is: ",result);

// Subtraction, -;
result = num1 - num2;
document.write("<br>Subtraction of two Numbers is: ",result);

// Multiplication, *;
result = num1 * num2;
document.write("<br>Multiplication of two Numbers is: ",result);

// Division, /;
result = num1 / num2;
document.write("<br>Division of two Numbers is: ",result);

// Modulus, %;
result = num1 % num2;
document.write("<br>Modulus of two Numbers is: ",result);

// Exponential, **;
result = num1 ** num2;
document.write("<br>Exponential of two Numbers is: ",result);

// increment and decrement operators
num1++;
document.write("<br>Increment of Num1 is: ",num1);
num1--;
document.write("<br>Decrement of Num1 is: ",num1);
